public class materi13 {
	public static void main (String[] args) {
	
	int i=0; 
	while (i <= 7){ // akan dicetak i selama i kurang dari sama dengan 7				
		System.out.println(i);
		i++; 						
		}
	}
}

/*
Penjelasan tentang program di atas 
Program diatas adalah proses looping pada nilai i
Perulangan while dipakai pada saat kita melakukan 
perulangan dengan jumlah yang belum diketahui pasti. 
Pernyataan pada while akan dikerjakan setelah pengecekan 
kondisi pada while bernilai true
Program diatas adalah looping pada nilai i, sama dengan di
materi12 akan dicetak output dari 1-7 dan berhenti apabila
i=8 karna yang akan dicetak apabila (i <=7)
*/